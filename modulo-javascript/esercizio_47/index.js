// Estrazione elementi base
// Dichiarazione di un array di numeri
const numeri = [15, 28, 38, 45, 56];

// Destructuring per assegnare i primi due elementi a due variabili
const [primo, secondo] = numeri;

// Stampa delle variabili nella console
console.log("Primo numero:", primo);  // Output: Primo numero: 15
console.log("Secondo numero:", secondo);  // Output: Secondo numero: 28
